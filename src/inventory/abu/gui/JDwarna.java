    private void Nobaris(){
        int baris = tabelwarna.getRowCount();
        for(int x = 0; x<baris; x++){
            String nomor = String.valueOf(x + 1);
           tabelwarna.setValueAt(nomor + ".", x,0);
        }
    }
    private void JudulTabel(){
        tabelwarna = new DefaultTableModel();
        tabelwarna.addColumn("NO");
        tabelwarna.addColumn("ID");
        tabelwarna.addColumn("NAMA WARNA");
        jTable1.setModel(tabelwarna);
        
        jTable1.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(50);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(267);
    }
    private void Isitabel(){
        tabelwarna.getDataVector().removeAllElements();
        tabelwarna.fireTableDataChanged();
        warnamodel.clear();
        warnamodel=warna.TampilWarna();
        for(int x=0; x<warnamodel.size(); x++){
            Object [] data = new Object[3];
            data[1] = warnamodel.get(x).getId();
            data[2] = warnamodel.get(x).getWarna();
            tabelwarna.addRow(data);
            Nobaris();
          
            
        }
        
        
    }
    private void BtnSimpanWarna(){
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Nama wara tidak boleh kososng !!");
            jTextField1.requestFocus();
        }else{
            warna.setWarna(jTextField1.getText());
            warna.TambahWarna();
            jTextField1.setText("");
            Isitabel();
        }
    }

